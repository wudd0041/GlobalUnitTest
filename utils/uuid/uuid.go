const alphabet = "123456789ABCDEFGHJKLMNPQRSTUVWXYZabcdefghijkmnopqrstuvwxyz"

func base58Encode(b []byte) string {
	x := new(big.Int)
	x.SetBytes(b)

	answer := make([]byte, 0, len(b)*136/100)
	for x.Cmp(bigZero) > 0 {
		mod := new(big.Int)
		x.DivMod(x, bigRadix, mod)
		answer = append(answer, alphabet[mod.Int64()])
	}

	// leading zero bytes
	for _, i := range b {
		if i != 0 {
			break
		}
		answer = append(answer, '1')
	}

	// reverse
	alen := len(answer)
	for i := 0; i < alen/2; i++ {
		answer[i], answer[alen-1-i] = answer[alen-1-i], answer[i]
	}

	return string(answer)
}

func base58Decode(b string) []byte {
	answer := big.NewInt(0)
	j := big.NewInt(1)

	scratch := new(big.Int)
	for i := len(b) - 1; i >= 0; i-- {
		tmp := b58[b[i]]
		if tmp == 255 {
			return []byte("")
		}
		scratch.SetInt64(int64(tmp))
		scratch.Mul(j, scratch)
		answer.Add(answer, scratch)
		j.Mul(j, bigRadix)
	}

	tmpval := answer.Bytes()

	var numZeros int
	for numZeros = 0; numZeros < len(b); numZeros++ {
		if b[numZeros] != '1' {
			break
		}
	}
	flen := numZeros + len(tmpval)
	val := make([]byte, flen, flen)
	copy(val[numZeros:], tmpval)

	return val
}

func genUUIDv4() []byte {
	var u = make([]byte, 16)
	_, err := rand.Read(u[:])
	if err != nil {
		panic(fmt.Sprintf("gen uuid error: %v", err))
	}
	// Set version
	u[6] = (u[6] & 0x0F) | (4 << 4)
	// Set variant bits
	u[8] = (u[8] | 0x40) & 0x7F
	return u
}

// 生成 Base58 表示的 RFC4122 V4 版本的 UUID，截取前 8 位
func UUID() string {
	return V4Compressed()[0:8]
}

// 在没有上下文的情况下判断字符串是否满足 Base58 uuid 的基本格式
func IsUUID(s string) bool {
	rb := []rune(s)
	for i := range rb {
		if !(unicode.IsUpper(rb[i]) || unicode.IsLower(rb[i]) || unicode.IsNumber(rb[i])) {
			return false
		}
	}
	return true
}

// 生成 RFC4122 V4 版本的 UUID，长度为 36
func V4() string {
	u := genUUIDv4()
	return fmt.Sprintf("%x-%x-%x-%x-%x", u[0:4], u[4:6], u[6:8], u[8:10], u[10:])
}

// 生成 Base58 表示的 RFC4122 V4 版本的 UUID，长度为 22
func V4Compressed() string {
	return base58Encode(genUUIDv4())
}

func DecompressV4(compressed string) string {
	u := base58Decode(compressed)
	return fmt.Sprintf("%x-%x-%x-%x-%x", u[0:4], u[4:6], u[6:8], u[8:10], u[10:])
}
